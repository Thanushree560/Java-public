class Arithmeticoperations
{
  public static void main(String args[])
  {
     int a,b,sum,diff,prod,div,mod;
     a=10;
     b=20;
    sum=a+b;
   diff =a-b;
   prod=a*b;
   div=a/b;
   mod=a%b;
   System.out.println("Sum="+sum+"\n Diff="+diff+"\n Prod="+prod+"\nDiv="+div+"\nMod="+mod);
 }
}